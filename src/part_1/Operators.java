package part_1;
public class Operators {
    public static void main(String[] args){
        // Operators
        int a = 1;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a%b);
        System.out.println(a>b);
        System.out.println(a<b);
    }
}
